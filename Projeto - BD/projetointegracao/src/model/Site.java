package model;

public class Site {

	private Integer id_site;
	private Integer id_iphone;
	private String url_site;
	private String nome_site;

	public int getId_site() {
		return id_site;
	}

	public void setId_site(int id_site) {
		this.id_site = id_site;
	}

	public Integer getId_iphone() {
		return id_iphone;
	}

	public void setId_iphone(Integer id_iphone) {
		this.id_iphone = id_iphone;
	}

	public String getUrl_site() {
		return url_site;
	}

	public void setUrl_site(String url_site) {
		this.url_site = url_site;
	}

	public String getNome_site() {
		return nome_site;
	}

	public void setNome_site(String nome_site) {
		this.nome_site = nome_site;
	}
	
}
