package model;


public class Camera {

	private Integer id_camera;
	private Integer id_iphone;
	private String resolucao_camera;
	
	
	public Integer getId_camera() {
		return id_camera;
	}
	public void setId_camera(Integer id_camera) {
		this.id_camera = id_camera;
	}
	public Integer getId_iphone() {
		return id_iphone;
	}
	public void setId_iphone(Integer id_iphone) {
		this.id_iphone = id_iphone;
	}
	public String getResolucao_camera() {
		return resolucao_camera;
	}
	public void setResolucao_camera(String resolucao_camera) {
		this.resolucao_camera = resolucao_camera;
	}
}
